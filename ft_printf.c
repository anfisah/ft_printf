#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	ap;
	int		count;

	count = 0;
	va_start(ap, format);
	count = ft_parse(ap, (char *)format);
	va_end(ap);
	return (count);
}
